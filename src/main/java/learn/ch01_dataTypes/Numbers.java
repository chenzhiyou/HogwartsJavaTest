package learn.ch01_dataTypes;

public class Numbers {
    public static void main(String[] args) {
        // 定义一个byte类型的变量
        byte byteByte = 100;
        System.out.println(byteByte);

        // 定义一个短整型
        short shortShort = 100;
        System.out.println(shortShort);

        //定义一个整型
        int intInt = 123456;
        System.out.println("int类型：" + intInt);

        // 定义一个长整型
        long longLong = 123456789L;
        System.out.println(longLong);
    }
}

package com.hogwarts.ch03_condition;

public class IfElseIf {
    public static void main(String[] args) {
        int hogwarts = 90;
        if (hogwarts >=80){
            System.out.println("优秀");
        }else if(hogwarts >=60){
            System.out.println("及格");
        }else {
            System.out.println("不及格");
        }
    }
}
